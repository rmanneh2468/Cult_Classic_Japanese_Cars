        <p>Description:</p>
        
        <p>The 1st generation, Lexus LS400, was a car that took nearly a decade to make.
        It is proof of the excellence that occurs, when one takes time, plans well, uses ingenuity, 
        and attention to detail. The project started in 1983, by Eiji Toyoda. It was called the F1 
        "Flagship No. 1 vehicle", or "Circle F" project. The F1 project, did not have a time scale 
        for completion, nor did it have budget constraints. 
        <br></br>
        As a result, the car was completely new. It did not use parts from the existing Toyota 
        parts bin. Furthermore, the final cost of development, for this ambitious luxury car, was over
        $1 Billion US dollars. Toyota symbolized their lofty aspirations, through this car, it was a 
        statment to the world. Toyota even created a new marque, named "Lexus", to use with the launch 
        of this machine, into the competitive, luxury car market. 
        <br></br>
        The LS400 had an all-new, V8 engine, called the 1UZ-FE. This engine is legendary
        for its smoothness, refined power, and excellent reliability. In fact, the engine is so impressive,
        that it was one of the first car engines, approved by the FAA, for use in aircraft. The car also 
        came equipped with a very robust, and long-lasting automatic transmission. The chassis had an 
        independent, double-wishbone suspension, or optional air suspension system. 
        <br></br>
        The 1st gen LS400, had a very quiet cabin. In fact, it is more quiet, than nearly every
        competitor, in the luxury car market, during that era. 
        Moreover, Toyota set the price of the LS400 at $35,000 USD, undercutting the competition. 
        The build quality of the 1st gen LS400 is exceptional. The reliability is also impressive, 
        as they pass 300,000 miles, on original engine and transmission, very often. The 1st gen Lexus
        LS400, generated a shockwave in the automotive industry, in the early 90's. Companies like
        BMW and Mercedes, were severely impacted, by the sucess of this iconic car. In fact, it took
        several years, for BMW and Mercedes, to recover from impact, of the 1st gen Lexus LS400.    
        </p>
